public class DeclareVariables { // Las clases de declaran con PascalCase
    public static void main(String[] args) {
        int myAge; // Las variables se declaran con camelCase
        myAge = 33; // inicializar una variable 
        String greeting = " Hello!";
        System.out.println(greeting + ", I am " + myAge + " years old." );
    }
}
